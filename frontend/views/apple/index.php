$js = <<<JS
$('.eat').click(function(e){
    e.preventDefault();
    let modal = $('#mymodal'); 
    modal.attr('data-href', $(this).attr('href'));
    modal.modal('show');
});
$(document).on('click', '#eat', function () {
    let modal = $('#mymodal'); 
    modal.find('.result').empty();
    $.ajax({
        url: modal.data('href') + '?piece=' + $('[name="piece"]').val(),
        success: function(response){
            if (response.percent_eaten == 100) {
                modal.find('.result').append('Яблоко полностью съедено!<br>')
            } else {
                modal.find('.result').append('Всего съедено ' + response.percent_eaten + '% яблока<br/>')
            }
            
        },
        error: function (data) {
            modal.find('.result').append(data.responseText + '<br/>')
        }
    });
})
JS;

$this->registerJs($js);
